     * Creates datatable
     */
    public function toDatatable()
    {
        $suppliers = Supplier::where('status', 1)->get();
        return Datatables::of($suppliers)
            ->addColumn('actions', function ($supplier) {
                return view('suppliers.partials.buttons', ['supplier' => $supplier]);
            })
            ->rawColumns(['actions'])
            ->make(true);
    }
